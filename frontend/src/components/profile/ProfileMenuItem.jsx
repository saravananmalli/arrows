export function getIcon(name) {
  return ICONS[name] ?? null
}

const ProfileMenuItem = memo(function ProfileMenuItem({
  icon,
  label,
  description,
  badge,
  shortcut,
  onClick,
  to,
  danger = false,
  disabled = false,
  showArrow = false,
}) {
  const navigate = useNavigate()

  const handleClick = (e) => {
    if (disabled) return
    if (onClick) onClick(e)
    if (to) navigate(to)
  }

  return (
    <button
      className={[
        'pmi',
        danger    ? 'pmi--danger'    : '',
        disabled  ? 'pmi--disabled'  : '',
      ].filter(Boolean).join(' ')}
      onClick={handleClick}
      disabled={disabled}
      aria-disabled={disabled}
      type="button"
    >
      {icon && (
        <span className="pmi-icon" aria-hidden="true">
          {typeof icon === 'string' ? getIcon(icon) : icon}
        </span>
      )}

      <span className="pmi-body">
        <span className="pmi-label">{label}</span>
        {description && <span className="pmi-desc">{description}</span>}
      </span>

      <span className="pmi-right">
        {badge && <span className="pmi-badge">{badge}</span>}
        {shortcut && <kbd className="pmi-shortcut">{shortcut}</kbd>}
        {showArrow && !badge && !shortcut && (
          <span className="pmi-arrow" aria-hidden="true">{ICONS['chevron-right']}</span>
        )}
      </span>
    </button>
  )
})

export default ProfileMenuItem
